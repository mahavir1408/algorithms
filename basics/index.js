const strings = ['a', 'b', 'c', 'd'];
console.log('Initial', strings);

//push: push an element at the end of an array
strings.push('e');
console.log('push', strings);

//pop: remove an element fromt the end of an array
strings.pop();
console.log('pop', strings);

//unshift: remove an element fromt the top of an array
strings.unshift();
console.log('unshift', strings);
